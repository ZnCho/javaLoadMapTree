package variable.ex;

public class VarEx1 {
	public static void main(String[] args) {
		//다음코드에반복해서나오는숫자 `4` ,`3` 을다른숫자로한번에변경할수있도록다음을변수 `num1` , `num2` 를 사용하도록 변경해보세요.
		int num1 = 4;
		int num2 = 3;
		System.out.println(num1 + num2);
		System.out.println(num1 - num2);
		System.out.println(num1 * num2);
	}
}
